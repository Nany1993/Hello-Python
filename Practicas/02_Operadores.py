#operadores aritmeticos

print(3+4) #suma
print(3-4)#resta
print(3/4)#división
print(3*4)#multiplicación
print(8%4)#modulo - la parte que sobra en una división
print(4//4) #división parte entera
print(3**4) #raiz

print("Hola" + "Python") #concatena textos

#operadores comparativos

print(3>4) #mayor que
print(3<4) #menor que
print(3==4) #igual que
print(3!=4)#diferente que
print(3>=4)#mayor o igual
print(3<=4)#menor o igual

